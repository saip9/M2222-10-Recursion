func factorial(_ n: Int) -> Int { // Start of the factorial function
    print("Calculating factorial" + "(" + "\(n)" + ")...") // Prints the first message according to the instructions using string interpoleration
    if (n <= 1) {
        print("Terminal case, returning 1.")
        return 1
    }
    else {
        let  result = n * factorial(n-1)
        print("Non-terminal case, returning \(result).")
        return result
    }
    
}
print("The final result is: \(factorial(n)).")

